import UIKit

extension UIImageView {
    func placeHolderPhoto() -> UIImage {
        UIImage(systemName: "photo.fill")!

    }
    
    func placeHolderAvatar() -> UIImage {
        UIImage(systemName: "person.fill")!
    }
}
